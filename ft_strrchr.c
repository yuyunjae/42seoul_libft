char	*ft_strrchr(const char *s, int c)
{
	char	*ans;

	ans = 0;
	while (s)
	{
		if (*s == c)
			ans = s;
		s++;
	}
	if (c == 0 && *s == 0)
		ans = s;
	return (ans);
}
